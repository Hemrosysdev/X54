#include <string.h>

#include "HttpServerQueue.h"
#include "FreeRtosQueueTask.h"
#include "UxLibCommon.h"

/*!************************************************************************************************************************************************************
 *
 *************************************************************************************************************************************************************/

HttpServerQueue::HttpServerQueue()
    : UxEspCppLibrary::FreeRtosQueue( 10,
                                      UxEspCppLibrary::FreeRtosQueueTask::minTargetQueueSize( sizeof( message_t ) ),
                                      "HttpServerQueue" )
{

}

/*!************************************************************************************************************************************************************
 *
 *************************************************************************************************************************************************************/

HttpServerQueue::~HttpServerQueue()
{
}

/*!************************************************************************************************************************************************************
 *
 *************************************************************************************************************************************************************/

BaseType_t HttpServerQueue::sendServerStart( void )
{
    return sendSimpleEvent( static_cast<uint32_t>( messageType_t::Start ) );
}

/*!************************************************************************************************************************************************************
 *
 *************************************************************************************************************************************************************/

BaseType_t HttpServerQueue::sendServerStop()
{
    return sendSimpleEvent( static_cast<uint32_t>( messageType_t::Stop ) );
}

