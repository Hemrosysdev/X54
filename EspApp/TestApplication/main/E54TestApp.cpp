void E54TestApp::create()
{
    printf( "\n"
            "=====================================================\n"
            "HEM-E54-TST-S02 Version %s from %s (%s)\n"
            "Build on branch %s\n"
            "Copyright 2019 by Ultratronik\n"
            "=====================================================\n\n",
            VERSION_NO, BUILD_DATE, DEV_STATE, BRANCH );


    vlogInfo( "NVS flash init" );
    esp_err_t ret = nvs_flash_init();
    if (ret == ESP_ERR_NVS_NO_FREE_PAGES || ret == ESP_ERR_NVS_NEW_VERSION_FOUND) {
        ESP_ERROR_CHECK(nvs_flash_erase());
        ret = nvs_flash_init();
    }
    ESP_ERROR_CHECK( ret );

    //Initialize TCP/IP stack
    vlogInfo( "init TCP/IP adapter" );
    tcpip_adapter_init();

    //Initialize event loop
    vlogInfo( "create event loop" );
    ESP_ERROR_CHECK( esp_event_loop_create_default() );

    // init and connect our own modules
    m_cliTask.createTask();
    m_cliUartTask.createTask();

    printf( "\n"
            "=====================================================\n"
            "Init done, start now scheduler\n"
            "=====================================================\n" );
}

